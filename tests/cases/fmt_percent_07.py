# % operator formatting
print("%s" % "hello")
print("%r" % "hello")
print("%d" % 42)
print("%5.2f" % 3.14159)
print("%x" % 255)
print("%X" % 255)
print("%o" % 8)
print("%%")
print("100%% done")
print("%s and %d" % ("str", 7))
print("%-10s|" % "left")
print("%10s|" % "right")
print("%+d" % 5)
print("%08.3f" % 2.5)
print("%e" % 31415.9)
print("%g" % 0.0001234)
print("%c" % 65)
print("%s=%d" % ("x", 10))
d = {"name": "dave", "age": 40}
print("%(name)s is %(age)d" % d)
print("%(age)05d" % d)
print("%i" % 99)
print("%#x" % 255)
