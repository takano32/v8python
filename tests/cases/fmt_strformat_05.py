# str.format positional, auto, named, conversions+spec
print("{0} {1}".format("a", "b"))
print("{1} {0}".format("a", "b"))
print("{} {}".format("x", "y"))
print("{name} is {age}".format(name="carol", age=30))
print("{0!r:>10}".format("hi"))
print("{0!s:<8}|".format(123))
print("{:.2f}".format(3.14159))
print("{:08.3f}".format(2.5))
print("{:+d}".format(7))
print("{:,}".format(1234567))
print("{:#x}".format(255))
print("{:b}".format(10))
print("{:e}".format(31415.9))
print("{:.3g}".format(0.0001234))
print("{:%}".format(0.42))
print("{0} {0} {1}".format("dup", "z"))
print("{:>{}}".format("r", 6))
print("{:.{}f}".format(3.14159, 3))
print("{val:^10}".format(val="mid"))
print("{0[1]}".format([10, 20, 30]))
d = {"k": "v"}
print("{0[k]}".format(d))
