# builtin format() function
print(format(3.14159, ".1f"))
print(format(3.14159, ".3f"))
print(format(255, "x"))
print(format(255, "#x"))
print(format(10, "b"))
print(format(1234567, ","))
print(format(42, "+d"))
print(format(3.14159, "08.3f"))
print(format(0.25, "%"))
print(format(0.25, ".1%"))
print(format(31415.9, "e"))
print(format(123.456, ".3g"))
print(format("hi", ">10"))
print(format("hi", "<10") + "|")
print(format("hi", "^10"))
print(format(7))
print(format(3.5))
print(format(255, "o"))
print(format(-42, "+d"))
print(format(1000, "_"))
